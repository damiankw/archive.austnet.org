      <p><strong>Austnet Services</strong></p>
      <p>AustNet provides it&#39;s users with client services, in the form of OPAS (Open Public Access Services). These services were written by co-founder and CTO, Roger Yerramsetti.<br /></p>
      <p><strong>ChanOP</strong><br />
        The channel service allows users to register their channels and maintain a secure method of issuing operator status. More than a mere &quot;bot&quot;, ChanOP is a server, effectively allowing it to maintain every aspect of your channel according to your wishes.<br />
      </p>
      <p><strong>NickOP</strong><br />
        The nickname service controls who has authorisation to use registered nicknames. You may elect to have NickOP enforce ownership of your nickname, so that those who do not know the password cannot use it. It can also store useful info such as your email address/website.<br />
      </p>
      <p><strong>NoteOP</strong><br />
        The message bank service allows you to leave notes to users with registered nicknames even when they are offline. For security, your notes can only be viewed using your password.<br />
      </p>
      <p><strong>LoveOP</strong><br />
        LoveOP has been designed for those with online love blossoming or who want to tell that special somebody that they care. It offers a rich command set, catering for everybody, from those who wish to remain secret admirers to couples who desire the union of IRC marriage.<br />
      </p>
      <p><strong>HelpOP</strong><br />
        The reference service holds information useful to people on the network, such as how to perform basic IRC functions, what the services do, what a bot is and which servers allow them.<br />
      </p>
      <p><strong>ASD</strong><br />
        ASD is the support service for the AustNet Services Department. It allows selected users the control they need over other services to assist people with problems. ASD can recover passwords for users who have forgotten them, transfer the ownership of a channel to a new person should the old manager leave IRC, and protect users from services abuse.<br />
      </p>
      <p><strong>Sprint</strong><br />
        This service allows network operators and administrators alike the ability to maintain the network at a server level. Network wide bans (G:Lines) and authorised triggers (certified mass-connections) are maintained through this service, as well as channel mode modifications and lockdowns.<br />
      </p>
      <p><strong>OperOP</strong><br />
        OperOP acts as a secondary support for this service in the case that Sprint is offline. These services are for authorised personnel only.</p>
