      <p><strong>About Us</strong></p>
      <p>Many people around the world connect to Internet Relay Chat (or IRC), which is an increasingly popular means of communication. First created in 1988, IRC has been developed further and rapidly grown since then to become what it is today -- a medium in which thousands of people can all choose where to chat, and what to chat about. Chatting to friends, family or just meeting new people across the world is made easy with IRC.</p>
      <p>Come and join Austnet - and leave behind the extensive lag, netsplits and channel takeovers of other networks. AustNet&#39;s top-class services allow you to register your own nickname and channel with no lengthy waits.</p>
      <p>Austnet is based upon the principle of an efficient, secure and well-developed network that allows users to own their channels and actively participate in the growth of an International network. Founded in Australia, AustNet has rapidly expanded through Canada, Singapore, Europe and the United States, providing international coverage with high quality servers. Austnet experiences minimal lag and netsplits and provides exceptional public services.</p>
      <p>Featuring superior technical innovations, AustNet excels in client services and online help.</p>
      <p><strong>Why Austnet?</strong></p>
      <p>AustNet is an international network that aims to be rid of server splits, political fatigue and nuking that ruins other IRC networks - finally a network that allows users to enjoy IRC. <br />In doing so, we offer technical innovations individual to our network. These include:</p>
      <ul>
        <li><strong>ChanOP (Channel Registration)</strong><br />
          The channel operator service allows users to register their channels and maintain a secure method of issuing operator status. More than a mere &quot;bot&quot;, ChanOP is a server, allowing it to operate from inside or outside the channel. This service allows the channel owner(s) the ability to keep their channel open 24 hours. </li>
        <li><strong>NickOP (Nickname Registration)</strong><br />
          The nickname registra controls who has authorisation to use certain nicknames. Upon connecting to any AustNet server, your nickname is verified with NickOP and if it is not yours, you are given the option to authenticate yourself or use another nickname. AustNet was the first known IRC network to implement a function for services to force a nick change, unlike other networks, which disconnect you if you fail to authenticate. NickOP has speedy registration facilities. </li>
        <li><strong>Minimal netsplits and lag</strong><br />
          AustNet servers are positioned strategically and are not normally subject to the lag and splits that plague other networks. The network does not have an &quot;open&quot; server link policy, and new servers are voted in based on their performance. </li>
        <li><strong>Friendly and ethical network operators</strong><br />
          AustNet network operators, or IRC Operators, are trained to the highest standards to meet the user&#39;s demands. Unlike other IRC networks, IRC Operators at AustNet are required to act according to a strict code of ethics.</li>
        <li><strong>Server innovations</strong><br />
          AustNet servers are based and located in the most suitable positions world wide. Alongside the well managed topology, servers run the most innovative server software of any network. Merging latest patches, combining the powerful code of other genres of IRCd, and developing our own code; AustNet&#39;s IRC daemon is acclaimed to be one of the most &quot;technically superior&quot; available. This gives you, the user, a better connection to the AustNet than any other network. Features include long nicknames (15 characters), full topic and mode synchronisation, the ability to display or hide channels without users, faster data bursting, anti-spam measures, in-channel services commands, long topics, and the world-first Virtual World to finally disperse nuking on IRC. </li>
      </ul>      </div>
