      <p><strong>Privacy</strong></p>
      <p>Collection of personal information by AustNet staff, such as email addresses and IP addresses shall not be harvested from NickOP info, scans and server notices.</p>
      <p>Personal email address requests are only requested for NickOP. This information is used to provide two services: Password retrieval and sending logs from ChanOP to channel owners.</p>
      <p>AustNet services only request NickOP information once when a user is registering their nickname. From time to time AustNet services may automatically check when a user logs in and identifies to ensure that their identification information is correct.</p>
      <p>This information, email addresses and passwords are kept on a secure server. AustNet staff are expected to keep information including ASD notices; helpme&rsquo;s; G:lines; klines; &amp; IP addresses they see in server notices and NickOP info private and not to be shared with NON AustNet staff.</p>
      <p>We advise users that we keep records of email addresses and IP addresses for identification purposes only and not for any other reasons.</p>
      <p>AustNet staff who are permitted to change email addresses may do so once they are satisfied that the user is the owner of the nickname and correct/amend or change the email address as/when appropriate.</p>
      <p>No staff member shall use information, which can be obtained through their helper status for their personal gain or to assist non AustNet staff for their personal gain.</p>
      <p>Information obtained from AustNet users will only be used for purposes of identification to the network, other requests for personal information from Non AustNet staff must be provided in writing to either asdadmin@austnet.org or admin@austnet.org.</p>
