      <p><strong>AustNet Services Department</strong></p>
      <p>The Austnet Services Department (ASD) maintain services (channel and nickname registration, amongst other matters) and therefore we have strict controls on how issues are handled. This page will give you a broad overview on how ASD deals with some situations.</p>
      <p><strong>Behaviour in #asd</strong><br />
        As a general guideline, #asd is for services questions only - those concerning chanop, nickop, noteop, loveop and helpop matters. If your question is not services related, you may be directed to another channel which can better help you. We also have rules in #asd that everyone is expected to abide by:</p>
      <ul>
        <li>No colours / colourful scripts</li>
        <li>No repeating or flooding</li>
        <li>No mp3 scripts</li>
        <li>No away scripts</li>
        <li>No loitering/idling</li>
        <li>No spam reports</li>
        <li>Gline/server ban/kill enquiries email <a href="mailto:&#104;&#101;&#108;&#112;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#104;&#101;&#108;&#112;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a></li>
        <li>No abusive or rude behaviour.</li>
      </ul>
      <p>Also, as this is a help channel, it is expected that behaviour will be &quot;G&quot;-rated - suitable for anyone to witness.</p>
      <p><strong>Password Retrieval</strong><br />
        ASD helpers are often requested to help with password retrieval, when users forget their nickname password. ASD can only help if the email address set for the nickname is a valid one, and the owner can access that email address. ASD helpers do see the email address for the nickname, but are not allowed to tell anyone that email address, including those who say they are the owner of the nickname. ASD helpers do not see the password for the nickname. Helpers cannot give the password out over IRC or send to another email address, so be sure your nickname email address is up to date.<br />
        <span class="commands">/msg nickop set email your@real-email-address</span></p>
      <p><strong>Channel Transfer</strong><br />
        ASD helpers can also help with transferring a channel&#39;s ownership from one nickname to another. To transfer a channel </p>
      <ul>
        <li>Both people must be online and identified; or (in the case of transferring to an alternate nickname) the owner must be identified as both nicknames;</li>
        <li>The new owner must accept the transfer;</li>
        <li>The new owner must not already own a channel;</li>
        <li>There must be no other irregularities (stolen channel etc).</li>
      </ul>
      <p><strong>Channel Owners Nickname Drops</strong><br />
      When a channel owner&#39;s nickname drops (through not being identified for 21 days) the nickname is automatically placed on the AustNet services banlist. This is to stop the channel being stolen by an opportunist re-registering the owner&#39;s nickname. ASD helpers will work out the &quot;next-in-line&quot; for the channel using such criteria as </p>
      <ul>
        <li>Highest level channel operators</li>
        <li>Operators that do not already own a channel</li>
        <li>Operators that were seen last in the channel.</li>
      </ul>
      <p>When the next-in-line is worked out, they are contacted by an ASD helper, and the channel is transferred to them. The owner&#39;s nickname is then removed from the Austnet services banlist, and may be registered by anyone.</p>
      <p><strong>Spamming</strong><br />
        Spamming is defined as unsolicited advertising of a channel, website or other IRC network. It usually falls to ASD to prevent spamming, and the definition of spam that we work from is a non-op advertising in a channel that does not want advertisements, OR a mass message / invitation to people who would not wish to join the channel. <br />
        What does this mean? </p>
      <ul>
        <li>A #chatzone op sending a user from #chatzone into #sex would not be spamming.</li>
        <li>A #chatzone non-op asking people in #chatzone to come to #melbteens would be spamming, because #chatzone do not allow that.</li>
        <li>A person on #chan telling people to join #wicked is allowed because the #chan ops allow advertising (very rare).</li>
      </ul>
      <p><strong>Any person on any channel using the /invite or /msg commands to invite non-friends is spamming. </strong></p>
      <p>The possible results of spamming are:</p>
      <ul>
        <li>Being banned from the channel you spammed in by an operator (not by ASD).</li>
        <li>Having your access removed and being banned from the channel you spammed, preferably by the channel owner. (Channel owners are expected to co-operate with ASD personel at all times).</li>
        <li>Having your channel suspended.</li>
        <li>Being removed from the network by an IRC Operator (not an ASD helper).</li>
        <li>Having an ASD administrator or IRC Operator contact your ISP if spamming continues.</li>
      </ul>
      <p><strong>Suspends</strong><br />
        A channel may be suspended for the following reasons:</p>
      <ul>
        <li>Spamming (as detailed above)</li>
        <li>Re-additions of spammers as soon as ASD leave the channel</li>
        <li>Advertising other IRC Networks in the topic (owners are given a chance to change the topic)</li>
        <li>Services Abuse: mass adduser/deluser to attract people to the channel; ban/seek/funmsg flooding; publishing personal details in topics and other breaches of AustNet policy.</li>
        <li>Framing of other channels by spamming or fake spam reports</li>
        <li>Using the channel as a base to attack other channels, or to collect clones. (This is usually used in conjunction with de-registration of the channel.)</li>
      </ul>
      <p><strong>Services Ignores</strong><br />
        Services ignores are used by ASD helpers to deny users the use of services for a time. Reasons for this can be</p>
      <ul>
        <li>Excess nicknames and/or channels;</li>
        <li>Channel takeovers or stealing nicknames/channels;</li>
        <li>Attempting to steal passwords;</li>
        <li>Continued spamming after warnings;</li>
        <li>Misuse of services (eg: using services to harrass other users).</li>
      </ul>
      <p>Services ignores are <em>not</em> set for abusing ASD helpers, but continued abuse may get you removed from Austnet by an IRC Operator, or your ISP (Internet provider) contacted.</p>      </div>
