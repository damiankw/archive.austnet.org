                  <td rowspan="2" valign="top"><img src="../images/t_feature.gif" width="350" height="22"><br>
                    <span class="channelheading">#chatzone</span><br> &nbsp;<br>
                    #Chatzone has been around since the beginning of AustNet and 
                    is currently a recommended channel within the AustNet Community. 
                    It was originally managed by Austnet, however as the network 
                    grew Chatzone was passed onto some of the channel operators 
                    at the time. In the years that followed there have been a 
                    few different owners. History buffs can view Chatzone's past 
                    at the history URL link.<br>
                    &nbsp;&nbsp;&nbsp;Chatzone is a general international chat 
                    channel on Austnet and is probably one of the largest channels 
                    in that grouping. We have operators (who operate under channel 
                    guidelines), and normal chatters from all over Australia and 
                    the world. Naturally Australia is well represented in the 
                    channel, but there is also a strong international presence 
                    from places like the USA, UK, Turkey, Pakistan, Malaysia, 
                    Singapore, Canada, Indonesia, New Zealand. Sometimes people 
                    drop in from Russia, Israel, France, India, Greece, Macedonia, 
                    Germany, The Netherlands, in fact the list is as endless as 
                    there are countries (Sorry if we missed your country!), You 
                    just never know who might be lurking in the channel or from 
                    where!<br>
                    &nbsp;&nbsp;&nbsp;Of the top 20 or so channels on AustNet, 
                    ChatZone is one of the least strict. Naturally there are rules 
                    against flooding, spamming etc, but we are more tolerant of 
                    the use of some colours, the playing of MP3 scripts, and we 
                    welcome chatters to state their a/s/l in channel and to attract 
                    a chat partner in that way. The name ChatZone says it all, 
                    and we encourage chat and opinions from beginners or old hands 
                    alike, regardless of age, gender, location or beliefs, so 
                    long as users are not abusive, and are following channel rules.<br>
                    &nbsp;&nbsp;There have been many ChatZone meets in the past, 
                    but being such a diverse group of people they are usually 
                    organised on a more individual basis depending on location. 
                    A number of people from overseas have also visited Australia 
                    and met up with channel regulars etc.<br>
                    &nbsp;&nbsp;&nbsp;The channel has a StatsOP bot which is very 
                    popular with the users, it can state a users chat statistics 
                    in the channel or for more detailed info people can view the 
                    full listing of stats on the channels website.<br>
                    You will find people in chatzone 24/7, especially when alot 
                    of the Australian population are asleep and other channels 
                    are almost empty!<br>
                    &nbsp;&nbsp;&nbsp;#chatzone has a bit of sentimental value 
                    on AustNet, so dont be shy, call in and say hello, who knows 
                    who you will meet!<br>
