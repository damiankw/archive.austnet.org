      <p><strong>Virtual World</strong></p>
      <p>IRC has become a warzone, this is undeniable truth. While new users are just learning to find their way around IRC, power hungry anti-social misfits are attempting to interrupt the ability of others to effectively communicate through IRC. While other IRC networks have attempted to deal with the effect, rather than the cause, to no avail, we have been able to implement measures that allow clients to chat with the knowledge they are safe from nukers while still using IRC in exactly the same manner.</p>
      <p>Dealing with the cause, rather than the effect of nuking, gives AustNet an individual method of dealing with this problem. AustNet has responded to the needs of its users by implementing software called Virtual World, which prevents nukers obtaining the information necessary to nuke. <br /></p>
      <p><strong>Who Can Use It?</strong></p>
      <p>Virtual World is available to all AustNet users. In order to utilise Virtual World, you will need to connect your IRC client to a server located near you (check the Server Catalogue). For further assistance on connecting to AustNet, read the New User Guide.</p>
      <p><strong>How does it work?</strong></p>
      <p>So how does it work? Well, we can assure you that it does. Each client is issued a &quot;Virtual World Account&quot; upon connection to the AustNet IRC Network. This account, or hostmask, is modified by an individual tracking number which makes it difficult to determine your real address. In order to active your Virtual World Account, you are required to have the user mode /mode nickname +v enabled. The usermode +v triggers our software to enabled your Virtual World Account. When you first connect to the AustNet IRC Network, your Virtual World Account is enabled by default. To disable it, and resume your normal userhost, type /mode nickname -v.</p>
      <p>Examples of how your Virtual World Account works: </p>
      <ul>
        <li>nickname!user@ppp.isp.com is your original userhost before you utilise Virtual World.</li>
        <li>nickname!user@vw-[account number].isp.com becomes your userhost while utilising the Virtual World software.</li>
      </ul>
      <p>Having a permanent Virtual World Account number ensures that functions like banning work normally. The account number is permanent and will always be the same every time you reconnect to the AustNet IRC Network, as long as your address stays the same. <br />When you do a /whois on your own nickname you will be able to see your real host or IP, however others will see the masked Virtual World Account address when they do the same command.</p>
      <p>Have any enquiries that are unanswered in this page? Feel free to contact AustNet through the following Contact AustNet page. If you require online assistance, this can be received via the #Help channel on the AustNet IRC Network.</p>      </div>
