      <p><strong>Contact Us</strong></p>
      <p>There are various methods of contacting AustNet based upon who the audience of your request should be. Simply click on the any of the following mail links to contact AustNet:</p>
      <p><strong>General Help and Support Requests</strong><br />For assistance with any AustNet related problems or issues, you can mail the AustNet Support Department and the network will reply as soon as possible. Topics normally include connection details, service assistance and general network information.</p>
      <blockquote>
        <p>Individual help requests should be mailed to <a href="mailto:&#104;&#101;&#108;&#112;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#104;&#101;&#108;&#112;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br /></p>
      </blockquote>
      <p><strong>Services Department</strong><br />The AustNet Services Department maintains a support service for the questions and discussion <em>specifically</em> related to AustNet services. This entails recommendations, enhancement notifications, channel and nickname registration disputes and/or mediation.</p>
      <blockquote>
        <p>Individual help requests should be mailed to <a href="mailto:&#115;&#101;&#114;&#118;&#105;&#099;&#101;&#115;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#115;&#101;&#114;&#118;&#105;&#099;&#101;&#115;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br /></p>
      </blockquote>
      <p><strong>Network Topology and Routing (Closed List)<br /></strong>The Routing Department discusses and manages AustNet&#39;s topology and server connectivity. It is responsible for server applications, maintenance and announcements.</p>
      <blockquote>
        <p>Individual help requests should be simply mailed to <a href="mailto:&#114;&#111;&#117;&#116;&#105;&#110;&#103;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#114;&#111;&#117;&#116;&#105;&#110;&#103;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br />This list is not available to the public for subscription.</p>
      </blockquote>
      <p><strong>Abuse Mediation</strong><br />AustNet encourages users who may be concerned about the actions of official AustNet staff to e-mail the abuse response service. The board of will resolve any concerns or issues you may have in a fair and independant manner, making sure that AustNet remains one of the best IRC networks around.</p>
      <blockquote>
        <p>Requests regarding IRCOps or Admin should be emailed to <a href="mailto:&#097;&#098;&#117;&#115;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#097;&#098;&#117;&#115;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br />Requests regarding ASD helpers should be emailed to <a href="mailto:&#097;&#115;&#100;&#097;&#100;&#109;&#105;&#110;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#097;&#115;&#100;&#097;&#100;&#109;&#105;&#110;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.</p>
      </blockquote>
      <p><strong>Public Relations</strong><br />The Public Relations department cooperatively organises network events on AustNet, as well as organising special channels for these events. In addition, the department also supervises the continuing development of the AustNet website, and supervises a news mailing list &#110;&#101;&#119;&#115;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;.</p>
      <blockquote>
        <p>To contact the Public Relations department (for example, to organise an event), email <a href="mailto:&#097;&#117;&#115;&#116;&#119;&#101;&#098;&#115;&#105;&#116;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#097;&#117;&#115;&#116;&#119;&#101;&#098;&#115;&#105;&#116;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br />A general discussion list on how to raise the profile of AustNet in a positive way is &#097;&#117;&#115;&#116;&#119;&#101;&#098;&#115;&#105;&#116;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;. To make suggestions for the website, email <a href="mailto:&#097;&#117;&#115;&#116;&#119;&#101;&#098;&#115;&#105;&#116;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#097;&#117;&#115;&#116;&#119;&#101;&#098;&#115;&#105;&#116;&#101;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br /></p>
      </blockquote>
      <p><strong>AustNet Development Mailing List</strong><br />AustNet maintains an IRCd development team mailing list for discussion pertaining to the development of the server software. Comments and suggestions for modifications can also be sent to this list.</p>
      <blockquote>
        <p>Individual suggestions/comments should be mailed to <a href="mailto:&#100;&#101;&#118;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;">&#100;&#101;&#118;&#064;&#097;&#117;&#115;&#116;&#110;&#101;&#116;&#046;&#111;&#114;&#103;</a>.<br /></p>
